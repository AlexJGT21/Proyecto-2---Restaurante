    private void cargarProductosAlCombo() {
        try {
            cbProductos.removeAllItems();
            listaProductosDisponibles = productoBO.obtenerProductos(); 
            for (ProductoDTO prod : listaProductosDisponibles) {
                cbProductos.addItem(prod.getNombre());
            }
        } catch (Exception e) {
            System.out.println("Error al cargar productos: " + e.getMessage());
        }
    }
    
    private void llenarTablaReceta(Long idProducto) {
        DefaultTableModel modelo = (DefaultTableModel) tbReceta.getModel();
        modelo.setRowCount(0); 
        
        try {
            
            List<ProductoIngredientes> receta = control.obtenerIngredientesPorProducto(idProducto);
            
            for (ProductoIngredientes pi : receta) {
                Object[] fila = {
                    pi.getIngredientes().getNombre(),
                    pi.getCantidad(),
                    pi.getIngredientes().getUnidad() 

                };
                modelo.addRow(fila);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error al cargar la receta: " + e.getMessage());
        }
    }
    
