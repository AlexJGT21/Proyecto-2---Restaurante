    private void btnCobrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCobrarActionPerformed
        int fila = tablaComandas.getSelectedRow();
        if (fila == -1) {
            JOptionPane.showMessageDialog(this, "Seleccione una comanda de la tabla para cobrar.");
            return;
        }
        
        // Agarramos el ID y el Total de la fila seleccionada
        Long idComanda = (Long) modeloComandas.getValueAt(fila, 0);
        String totalTexto = (String) modeloComandas.getValueAt(fila, 4);
        
        
        // Abrimos la pantalla de cobro
        this.dispose();
        control.mostrarCobrarComandaFORM(idComanda, totalTexto);
    }//GEN-LAST:event_btnCobrarActionPerformed

