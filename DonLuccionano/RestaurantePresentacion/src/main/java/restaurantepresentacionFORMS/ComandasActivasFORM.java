    private void configurarTabla() {
        // Colocamos los datos en la tabla
        modeloComandas = new DefaultTableModel(
                new Object[]{"ID", "Folio", "Mesa", "Cliente", "Total Actual"}, 0
        );
        tablaComandas.setModel(modeloComandas);

        // Opcional: Ocultar la columna ID para que el usuario no la vea, pero nosotros sí la usemos
        tablaComandas.getColumnModel().getColumn(0).setMinWidth(0);
        tablaComandas.getColumnModel().getColumn(0).setMaxWidth(0);
        tablaComandas.getColumnModel().getColumn(0).setWidth(0);
    }

    private void llenarTablaActivas() {
        modeloComandas.setRowCount(0); // Limpiar tabla

        try {
            // Traemos las comandas de la BD
            java.util.List<restaurantedominio.Comanda> listaActivas = control.obtenerComandasActivas();

            for (restaurantedominio.Comanda c : listaActivas) {
                // Validamos si tiene cliente o es público general
                String nombreCliente = (c.getCliente() != null)
                        ? c.getCliente().getNombre() + " " + c.getCliente().getApellidoPaterno()
                        : "Público General";

                modeloComandas.addRow(new Object[]{
                    c.getId(), // El ID oculto
                    c.getFolio(),
                    "Mesa " + c.getMesa().getNumMesa(),
                    nombreCliente,
                    "$" + c.getTotalVenta()
                });
            }
        } catch (restaurantenegocio.NegocioException ex) {
            JOptionPane.showMessageDialog(this, "Error al cargar las comandas: " + ex.getMessage());
        }
    }
    
