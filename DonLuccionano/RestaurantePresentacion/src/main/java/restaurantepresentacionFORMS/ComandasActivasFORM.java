    private void btnEditarOrdenActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarOrdenActionPerformed
        int fila = tablaComandas.getSelectedRow();
        if (fila == -1) {
            javax.swing.JOptionPane.showMessageDialog(this, "Seleccione una comanda de la tabla para editarla.");
            return;
        }
        
        // Agarramos el ID oculto
        Long idComanda = (Long) modeloComandas.getValueAt(fila, 0);
        
        // Cerramos esta pantalla y abrimos la de edición
        this.dispose();
        control.mostrarEditarComandaFORM(idComanda);
    }//GEN-LAST:event_btnEditarOrdenActionPerformed

